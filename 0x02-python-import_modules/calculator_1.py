#!/usr/bin/python3
def add(a, b):
    """My adding funct

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My subtracting function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplying funct

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """My dividing funct

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a / b
    """
    return int(a / b)
